package Gun04;

public class _01_JavaString {
    public static void main(String[] args) {
        int sayac=0; // int tipi, sayac adı, 0 başlangıç değeri

        String ad="İsmet"; // keşime veya cümle saklayacaksan string kullanırsın

        System.out.println("sayac = " + sayac);
        System.out.println("ad = " + ad);

        String soyad="Temur";
        System.out.println("soyad = " + soyad);

        String tamAd=ad +" "+ soyad; // + işareti sayılarda toplar, kelimelerde birleştirir
        System.out.println("tamAd =  " + tamAd);






    }
}
