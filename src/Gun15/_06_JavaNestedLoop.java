package Gun15;

public class _06_JavaNestedLoop {
    public static void main(String[] args) {

        // 2'lerin çarpı tablosunun 5'ler dahil olacak versiyonunu yap


        for (int kaclar = 1; kaclar <= 5; kaclar++) {

            for (int i = 1; i <= 10; i++) {
                System.out.println(kaclar + "x" + i + "=" + (kaclar * i));

            }


        }


    }
}
