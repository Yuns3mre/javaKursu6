package Gun11;

public class _07_JavaMath {
    public static void main(String[] args) {

        // Hazır fonksiyonlar hesap makinası gibi düşün

        int a=-12;
        int b=4;

        System.out.println("a'nın mutlak değeri= " + Math.abs(a)); // abs= | mutlak |
        System.out.println("a ve b'nin büyük olanını al=" + Math.max(a,b));
        System.out.println("a ve b'nin büyük olanını al=" + Math.min(a,b));
        System.out.println("2'nin 3. kuvvetini al=" + Math.pow(2,3)); // pow= üslü ifade
        System.out.println("b'nin karekökünü al=" + Math.sqrt(b));
        System.out.println("round(yuvarlama) 3.1=" + Math.round(3.1));
        System.out.println("round(yuvarlama) 3.5=" + Math.round(3.5));

        System.out.println("ceil 3.1 =" + Math.ceil(3.1)); // ceil : Kendisinden büyük en küçük tam sayı
        System.out.println("floor 3.1 =" + Math.floor(3.1)); // floor : Kendisinden küçük en büyük tam sayı













    }
}
