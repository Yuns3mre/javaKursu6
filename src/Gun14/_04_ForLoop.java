package Gun14;

public class _04_ForLoop {
    public static void main(String[] args) {

        // 0'dan 10 (dahil) kadar sayıları ekrana yazdırınız
        // 10'dan 0 (dahil) kadar sayıları ekrana yazdırınız




        for (int i=0; i<=10; i++)
        {
            System.out.println("sayac = " + i);
        }



        for (int i = 10; i >= 0; i--)
        {
            System.out.println("sayac = " + i);
        }









    }
}
